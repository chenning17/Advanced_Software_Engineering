package cafepackage;

public class SimulationTime {
	private int hours, minutes, seconds;
	private int year, month, day;
	
	public SimulationTime(int hours, int minutes, int seconds, int year, int month, int day) {
		this.hours = hours;
		this.minutes = minutes; 
		this.seconds = seconds;
		this.year = year;
		this.month = month;
		this.day = day;
	}
}
